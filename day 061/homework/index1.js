// 1) Math.random() — შემთხვევითი რიცხვი 0-დან 1-მდე

let randomNumber = Math.random();

console.log(randomNumber);






// 2) Math.floor() — რიცხვის დამრგვალება ქვემოთ

let decimalNumber = 4.9;

let roundedDown = Math.floor(decimalNumber);

console.log(roundedDown); // 4






// 3) რამდენი ელემენტია გაგება .lenght

let numbers = [10, 20, 30, 40, 50];

console.log(numbers.length); // 5






// 4) push() — ახალი ელემენტის დამატება ლისტის ბოლოს

let fruits = ["apple", "banana"];

fruits.push("orange");

console.log(fruits); // ["apple", "banana", "orange"]





// 5) pop() — ბოლო ელემენტის წაშლა ლისტის ბოლოს

let cars = ["BMW", "Audi", "Mercedes"];

cars.pop();

console.log(cars); // ["BMW", "Audi"]




