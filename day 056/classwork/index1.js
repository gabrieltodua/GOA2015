

let state1 = 0;

function on_off_function1() {
    let box = document.getElementById("on_off1");

    if (state1 === 0) {
        box.style = "width:50px; height:25px; background-color: rgba(232, 24, 24, 0.838); border-radius:15px; border:2px solid black; border-right:10px solid white;";
        state1 = 1;
    } else {
        box.style = "width:50px; height:25px; background-color: rgba(255, 255, 255, 0.838); border-radius:15px; border:2px solid black; border-left:10px solid white;";
        state1 = 0;
    }
}








